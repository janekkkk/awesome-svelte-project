<h1>Webpack template for complex building SPA using Svelte</h1>
<br>
<h2>features:</h2>

<ul>
  <li> 🔄 live reloading</li>
  <li> 🔎 analyze mode for detecting large dependencies</li>
  <li> 🐣 understandable structure folders</li>
  <li> 🌈 postcss for your styles</li>
  <li> 🧹 ESlint and Prettier for formatting and lint your code </li>
  <li> 🤓 images, fonts and static support</li>
  <li> 🚀 blazing fast loading google fonts</li>
  <li> 🚗 support dynamic load pages with page.js router</li>
</ul>

